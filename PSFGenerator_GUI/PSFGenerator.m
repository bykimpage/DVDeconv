% --- Executes on button press in pushbutton1.
function pushbutton1_Callback(hObject, eventdata, handles)
% hObject    handle to pushbutton1 (see GCBO)
% eventdata  reserved - to be defined in a future version of MATLAB
addstr = ['- PSF generation start'];
str = cat( 1, {addstr});
set(handles.listbox1, 'String',str);
drawnow;
tic;
    em = str2num(get(handles.edit3,'string'));
    na = str2num(get(handles.edit4,'string'));
    xyres = str2num(get(handles.edit5,'string'));
    zres = str2num(get(handles.edit6,'string'));
    xysize = str2num(get(handles.edit7,'string'));
    zsize = str2num(get(handles.edit8,'string'));
    rs = str2num(get(handles.edit9,'string'));
    ri = str2num(get(handles.edit15,'string'));
    dstart = str2num(get(handles.edit10,'string'));
    numPSF = str2num(get(handles.edit16,'string'));
    w4 = str2num(get(handles.edit19,'string'));
    w7 = str2num(get(handles.edit20,'string'));
    w8 = str2num(get(handles.edit13,'string'));
    w12 = str2num(get(handles.edit14,'string'));
    generatePSF(em,na,xyres,zres,xysize,zsize,rs,ri,dstart,numPSF,w4, w7, w8,w12);
t = toc;
addstr = ['- End, running time: ' num2str(t) 's'];
str = cat( 1, {addstr}, str );
set(handles.listbox1, 'String',str);
drawnow;
